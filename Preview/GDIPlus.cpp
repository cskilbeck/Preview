#include "stdafx.h"

//////////////////////////////////////////////////////////////////////

#pragma comment(lib, "gdiplus.lib")
using namespace Gdiplus;

//////////////////////////////////////////////////////////////////////

namespace
{
	GdiplusStartupInput gdiplusStartupInput;
	ULONG_PTR gdiplusToken;
}

//////////////////////////////////////////////////////////////////////

GDI GDI::initializer;

//////////////////////////////////////////////////////////////////////

GDI::GDI()
{
	GdiplusStartup(&gdiplusToken, &gdiplusStartupInput, NULL);
}

//////////////////////////////////////////////////////////////////////

GDI::~GDI()
{
	GdiplusShutdown(gdiplusToken);
}
